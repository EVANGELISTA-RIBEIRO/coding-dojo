import pytest
from dojo import tower


@pytest.mark.parametrize(
    ['entrance', 'expected'],
    (
        (1, ['*']),
        (2, [' * ', '***']),
        (3, ['  *  ', ' *** ', '*****']),
        (6, ['     *     ', '    ***    ', '   *****   ', '  *******  ', ' ********* ', '***********']),
        (
            11,
            [
                '          *          ',
                '         ***         ',
                '        *****        ',
                '       *******       ',
                '      *********      ',
                '     ***********     ',
                '    *************    ',
                '   ***************   ',
                '  *****************  ',
                ' ******************* ',
                '*********************',]
        ),
        (
            20,
            [
                '                   *                   ',
                '                  ***                  ',
                '                 *****                 ',
                '                *******                ',
                '               *********               ',
                '              ***********              ',
                '             *************             ',
                '            ***************            ',
                '           *****************           ',
                '          *******************          ',
                '         *********************         ',
                '        ***********************        ',
                '       *************************       ',
                '      ***************************      ',
                '     *****************************     ',
                '    *******************************    ',
                '   *********************************   ',
                '  ***********************************  ',
                ' ************************************* ',
                '***************************************',
            ]
        ),
    )
)
def test_tower(entrance, expected):
    assert tower(entrance) == expected
